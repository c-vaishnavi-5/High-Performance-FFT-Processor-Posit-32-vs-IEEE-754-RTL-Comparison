module fp_multiplication_clk_tb;

    reg         clk;
    reg         rst_n;
    reg  [31:0] A;
    reg  [31:0] B;
    wire [31:0] Product;

    fp_multiplication_clk dut (
        .clk(clk),
        .rst_n(rst_n),
        .A(A),
        .B(B),
        .Product(Product)
    );
    always #5 clk = ~clk;

    initial begin
        clk   = 1'b0;
        rst_n = 1'b0;
        A     = 32'h00000000;
        B     = 32'h00000000;

        $monitor("time=%0t | A=%h | B=%h | Product=%h",
                  $time, A, B, Product);

        #20 rst_n = 1'b1;

        // 1) 2.0 * 3.0 = 6.0
        // 2.0 = 0x40000000, 3.0 = 0x40400000, 6.0 = 0x40C00000
        #10;
        A = 32'h40000000;
        B = 32'h40400000;
        #20;

        // 2) 1.5 * 2.5 = 3.75
        // 1.5 = 0x3FC00000, 2.5 = 0x40200000, 3.75 = 0x40700000
        A = 32'h3FC00000;
        B = 32'h40200000;
        #20;

        // 3) -2.0 * 3.0 = -6.0
        // -2.0 = 0xC0000000, 3.0 = 0x40400000, -6.0 = 0xC0C00000
        A = 32'hC0000000;
        B = 32'h40400000;
        #20;

        // 4) -2.5 * -4.0 = 10.0
        // -2.5 = 0xC0200000, -4.0 = 0xC0800000, 10.0 = 0x41200000
        A = 32'hC0200000;
        B = 32'hC0800000;
        #20;

        // 5) 10.0 * 0.5 = 5.0
        // 10.0 = 0x41200000, 0.5 = 0x3F000000, 5.0 = 0x40A00000
        A = 32'h41200000;
        B = 32'h3F000000;
        #20;

        // 6) 0.0 * 7.0 = 0.0
        // 7.0 = 0x40E00000
        A = 32'h00000000;
        B = 32'h40E00000;
        #20;

        // 7) -0.0 * 3.5 -> treated as zero in this design
        // -0.0 = 0x80000000, 3.5 = 0x40600000
        A = 32'h80000000;
        B = 32'h40600000;
        #20;

        // 8) +inf * 2.0 = +inf
        // +inf = 0x7F800000, 2.0 = 0x40000000
        A = 32'h7F800000;
        B = 32'h40000000;
        #20;

        // 9) +inf * -3.0 = -inf
        // -3.0 = 0xC0400000, result sign = 1 => 0xFF800000
        A = 32'h7F800000;
        B = 32'hC0400000;
        #20;

        // 10) 1e-3 * 1e3 ≈ 1.0
        // 1e-3 ≈ 0x3A83126F, 1e3 ≈ 0x447A0000
        A = 32'h3A83126F;
        B = 32'h447A0000;
        #20;

        // 11) 0.75 * 0.5 = 0.375
        // 0.75 = 0x3F400000, 0.5 = 0x3F000000, 0.375 ≈ 0x3EC00000
        A = 32'h3F400000;
        B = 32'h3F000000;
        #20;

        // 12) -1.25 * 4.0 = -5.0
        // -1.25 = 0xBFA00000, 4.0 = 0x40800000, -5.0 = 0xC0A00000
        A = 32'hBFA00000;
        B = 32'h40800000;
        #20;

        // 13) 1.0 * 0.0 = 0.0
        // 1.0 = 0x3F800000
        A = 32'h3F800000;
        B = 32'h00000000;
        #20;
        // max finite ≈ 0x7F7FFFFF, 1.0 = 0x3F800000
        A = 32'h7F7FFFFF;
        B = 32'h3F800000;
        #20;
        // 1e-10 ≈ 0x3DCCCCCD
        A = 32'h3DCCCCCD;
        B = 32'h3DCCCCCD;
        #20;

        #40;
        $stop;
    end

endmodule
module fp_addsub_clk_tb;

    reg         clk;
    reg         rst_n;
    reg  [31:0] A;
    reg  [31:0] B;
    reg         op;   
    wire [31:0] Sum;

    fp_addsub_clk dut (
        .clk(clk),
        .rst_n(rst_n),
        .A(A),
        .B(B),
        .op(op),
        .Sum(Sum)
    );


    always #5 clk = ~clk;

    initial begin
        clk   = 1'b0;
        rst_n = 1'b0;
        A     = 32'h00000000;
        B     = 32'h00000000;
        op    = 1'b0;


        $monitor("time=%0t | A=%h | B=%h | op=%b | Sum=%h",
                  $time, A, B, op, Sum);


        #20 rst_n = 1'b1;

        // 1) 1.0 + 1.0
        // A = 1.0 (0x3F800000), B = 1.0
        #10;
        A  = 32'h3F800000;
        B  = 32'h3F800000;
        op = 1'b0;   // add
        #20;

        // 2) 1.5 + 2.5
        // 1.5 = 0x3FC00000, 2.5 = 0x40200000
        A  = 32'h3FC00000;
        B  = 32'h40200000;
        op = 1'b0;
        #20;

        // 3) 5.0 + (-3.0)
        // 5.0 = 0x40A00000, -3.0 = 0xC0400000
        A  = 32'h40A00000;
        B  = 32'hC0400000;
        op = 1'b0;
        #20;

        // 4) -2.5 + -3.5
        // -2.5 = 0xC0200000, -3.5 = 0xC0600000
        A  = 32'hC0200000;
        B  = 32'hC0600000;
        op = 1'b0;
        #20;

        // 5) 10.0 + 0.5
        // 10.0 = 0x41200000, 0.5 = 0x3F000000
        A  = 32'h41200000;
        B  = 32'h3F000000;
        op = 1'b0;
        #20;

        // 6) 8.0 - 2.0  (op = 1 => A - B)
        // 8.0 = 0x41000000, 2.0 = 0x40000000
        A  = 32'h41000000;
        B  = 32'h40000000;
        op = 1'b1;   // subtract
        #20;

        // 7) 4.0 - 1.0
        // 4.0 = 0x40800000, 1.0 = 0x3F800000
        A  = 32'h40800000;
        B  = 32'h3F800000;
        op = 1'b1;
        #20;

        // 8) 1.0 + (-1.0) => should go to zero
        // -1.0 = 0xBF800000
        A  = 32'h3F800000;
        B  = 32'hBF800000;
        op = 1'b0;
        #20;

        // 9) -1.0 + 1.0 => zero
        A  = 32'hBF800000;
        B  = 32'h3F800000;
        op = 1'b0;
        #20;

        // 10) 2.0 - 2.0 => zero
        A  = 32'h40000000;
        B  = 32'h40000000;
        op = 1'b1;
        #20;

        // 11) 3.5 - 1.5 = 2.0
        // 3.5 = 0x40600000, 1.5 = 0x3FC00000
        A  = 32'h40600000;
        B  = 32'h3FC00000;
        op = 1'b1;
        #20;

        // 12) -4.0 + 1.0
        // -4.0 = 0xC0800000
        A  = 32'hC0800000;
        B  = 32'h3F800000;
        op = 1'b0;
        #20;

        // 13) 1.0 - 4.0 = -3.0
        A  = 32'h3F800000;
        B  = 32'h40800000;
        op = 1'b1;
        #20;

        // 14) 0.0 + 5.0
        // 5.0 = 0x40A00000
        A  = 32'h00000000;
        B  = 32'h40A00000;
        op = 1'b0;
        #20;

        // 15) -0.0 + 2.0
        // -0.0 = 0x80000000, 2.0 = 0x40000000
        A  = 32'h80000000;
        B  = 32'h40000000;
        op = 1'b0;
        #20;

        #40;
        $stop;
    end

endmodule